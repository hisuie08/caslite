class Week {
  // 週間天気予報。
  final String srf;
  final String week;
  final String amedas;
  const Week({required this.srf, required this.week, required this.amedas});
  static List<Week> getWeeks(String srf) {
    final List<Week> result = [];
    for (var sf in _srf2weeks.where((e) => e.srf == srf)) {
      for (var w in sf.weeks) {
        result.addAll(_weeks.where((e) => e.week == w));
      }
    }
    return result;
  }
}

